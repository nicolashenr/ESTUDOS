var minhaVar = 67;
minhaVar = minhaVar + 1;

//Uma forma de encurtar esse código em javascript é usar o "++"

//portanto

minhaVar = minhaVar + 1;

//fica igual, se escrevermos:

minhaVar++;
console.log(minhaVar); //Console deve mostrar 70 para a variável "minhaVar"



//AGORA DECREMENTANDO UM NÚMERO

minhaVar--;

//Repare que pode ser usado da mesma forma que a soma

//PONTOS FLOAT - FLUTUANTES, são números por vírgula, que na linguagem javascript é usado o ".", como em 5.1, 9.0, 2.0005, etc...

minhaVar = minhaVar + 1.0;
console.log(minhaVar)

//Podemos dividar números float, ou decimáis também

minhaVar = minhaVar / 2.0;
console.log(minhaVar);

//Encontrando o resto de uma divisão, geralmente usado para descobrir se o resultado é par, o símbolo usado é "%"

minhaVar = minhaVar % 2;
console.log(minhaVar);



